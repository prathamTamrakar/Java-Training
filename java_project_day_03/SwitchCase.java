class SwitchCase
{
	public static void main(String args[])
	{
		int n=2;
		switch(n)
		{
			case 1: System.out.println("student is CS - A section");
			break;

			case 2: System.out.println("student is CS - B section");
			break;

			case 3: System.out.println("student is CS - C section");
			break;

			default : System.out.println("enter valid choice between 1,2 and 3");
			break;
		}

			
	}
} 